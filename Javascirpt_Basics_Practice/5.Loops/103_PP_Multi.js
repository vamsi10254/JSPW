// 1.	Print numbers 1 to 10

// for loop
for(let i=1;i<=10;i++){
    console.log(i);
}

// while loop
let i=1;
while(i<=10){
    console.log(i);
    i++;
}

// do while
let j=1;
do {
   console.log(j);
    j++;
}while(j<=10)

   
