console.log(5 == "5");  // true
console.log(5 === "5"); // false
console.log(10 > 5);
console.log(10 <= 10);

// Comparison operators

let x = 10;
let y = "10";

console.log(x == y);   // true (value only)
console.log(x === y);  // false (value + type)
console.log(x != y);   // false
console.log(x > 5);    // true
